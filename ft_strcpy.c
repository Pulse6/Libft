#include "libft.h"

char				*ft_strcpy(char *dest, const char *src)
{
	int		i;
	char	*cpy;

	i = 0;
	cpy = dest;
	while (src[i])
	{
		cpy[i] = src[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
