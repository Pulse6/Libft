#include "libft.h"

char				*ft_strchr(const char *s, int c)
{
	int		i;
	char	c1;
	char	*s2;

	i = 0;
	c1 = (char)c;
	s2 = (char *)s;
	while (s2[i] && s2 != '\0')
	{
		if (s2[i] == c1)
			return (s2 + i);
		i++;
	}
	if (c1 == '\0')
		return (s2 + i);
	return (NULL);
}
